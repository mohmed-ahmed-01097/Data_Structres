int main(){
	STACK_DataType data;
	STACK_tstr* S = NULL_PTR;
	initStack(&S);

	if(S){
		push(S,10);
		push(S,20);
		push(S,30);
		push(S,40);
		push(S,50);
		push(S,60);
		push(S,70);
		push(S,80);
		push(S,90);
		push(S,100);

		stack_check(S);

		printf("\\======================================================\\\n");

		stack_check(S);

		printf("\\======================================================\\\n");

		top(S,&data);
		stack_check(S);

		printf("\\======================================================\\\n");

		stack_display(S);

		printf("\\======================================================\\\n");

		stack_check(S);

		printf("\\======================================================\\\n");

		push(S,10);
		push(S,20);
		push(S,30);
		push(S,40);
		push(S,50);
		push(S,60);

		stack_check(S);
		stack_display(S);

		pop(S,&data);
		pop(S,&data);
		push(S,400);
		push(S,500);
		push(S,600);

		stack_check(S);
		stack_display(S);

		printf("\\======================================================\\\n");

		clrStack(S);
		pop(S,&data);
		top(S,&data);

		printf("\\======================================================\\\n");

		deinitStack(S);

		printf("\\======================================================\\\n");

	}else{
		printf("the Stack is failed to be initialized\n\n");
	}


	return 0;
}

